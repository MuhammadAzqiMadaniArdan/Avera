export type ProductStatus = "limited" | "public";
export type ProductStatusKey = "action" | "review" | "hidden";

export interface Product {
  id: number;
  name: string;
  category: string;
  storeSlug: string;
  price: number;

  image: string; // primary image
  images: string[]; // gallery

  sold: number;
  stock: number;
  weight: number; // gram

  rating: number; // 1â€“5
  reviews: number; // total review count

  description: string;

  slug: string;
  status: ProductStatus;
  sku: string;
  statusKey: ProductStatusKey;
}
const STATUS_KEYS: ProductStatusKey[] = ["action", "review", "hidden"];

function getRandomStatusKey(): ProductStatusKey {
  return STATUS_KEYS[Math.floor(Math.random() * STATUS_KEYS.length)];
}

